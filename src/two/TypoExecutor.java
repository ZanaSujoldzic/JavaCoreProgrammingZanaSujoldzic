public class TypoExecutor {
    public static void main(String[] args) {
        int number = 26; //dekadni sistem
        System.out.println(number);
        number = 0x1a; //0x heksadecimalni sistem
        System.out.println(number);
        number = 0b11010; //0b binarni brojni sistem
        System.out.println(number);
        number = 032; //0 oktalni brojni sistem
        System.out.println(number);
    }
 
}
